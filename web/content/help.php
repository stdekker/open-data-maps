<p>Welkom bij Open Data Maps. Een korte instructie:</p>
<ul>
    <li>Gebruik de zoekbalk om een gemeente te vinden</li>
    <li>Via het icoon met de drie puntjes naast de gemeentenaam pas je de statistische instellingen van de kaart aan.</li>
    <li>Dubbelklik op een gemeente in de landelijke weergave om in te zoomen</li>
    <li>Control + klik om een gebied op de kaart te selecteren.</li>
    <li>Hover over gebieden voor meer informatie.</li>
</ul>

